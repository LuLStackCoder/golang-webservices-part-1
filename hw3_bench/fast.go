func FastSearch(out io.Writer) {
	file, err := os.Open(filePath)
	if err != nil {
		panic(err)
	}

	fileScanner := bufio.NewScanner(file)
	if err != nil {
		panic(err)
	}

	seenBrowsers := make(map[string]struct{})
	i := -1
	user := User{}
	fmt.Fprintln(out, "found users:")
	for fileScanner.Scan() {
		i++

		bts := fileScanner.Bytes()
		if !bytes.Contains(bts, []byte("Android")) {
			if !bytes.Contains(bts, []byte("MSIE")) {
				continue
			}
		}
		err := user.UnmarshalJSON(bts)
		if err != nil {
			panic(err)
		}

		isAndroid := false
		isMSIE := false

		for j := range user.Browsers {
			if strings.Contains(user.Browsers[j], "Android") {
				isAndroid = true
				seenBrowsers[user.Browsers[j]] = struct{}{}
			}
			if strings.Contains(user.Browsers[j], "MSIE") {
				isMSIE = true
				seenBrowsers[user.Browsers[j]] = struct{}{}
			}
		}

		if isAndroid && isMSIE {
			email := strings.Replace(user.Email, "@", " [at] ", 1)
			fmt.Fprintf(out, "[%d] %s <%s>\n", i, user.Name, email)
		}

	}

	fmt.Fprintln(out, "\nTotal unique browsers", len(seenBrowsers))
}

